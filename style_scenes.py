def normalize_scene(scene: str | None) -> str:
    value = (scene or "").strip()
    return value if value in ALLOWED_SCENES else DEFAULT_SCENE


def get_scene_rules(scene: str | None) -> str:
    return STYLE_SCENES[normalize_scene(scene)]
